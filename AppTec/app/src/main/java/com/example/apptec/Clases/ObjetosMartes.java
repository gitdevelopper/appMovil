package com.example.apptec.Clases;

public class ObjetosMartes {

    private String Materia;
    private String Profesor;
    private String App;
    private String Apm;
    private String Grado;
    private String Credito;
    private String Clave;
    private String Opcion;
    private String Dia;
    private String Hinicio;
    private String Hfin;
    private String Aula;



    public ObjetosMartes(){

    }

    public ObjetosMartes(String materia, String profesor, String app, String apm, String grado, String credito, String clave, String opcion, String dia, String hinicio, String hfin, String aula) {
        Materia = materia;
        Profesor = profesor;
        App = app;
        Apm = apm;
        Grado = grado;
        Credito = credito;
        Clave = clave;
        Opcion = opcion;
        Dia = dia;
        Hinicio = hinicio;
        Hfin = hfin;
        Aula = aula;
    }

    public String getMateria() {
        return Materia;
    }

    public void setMateria(String materia) {
        Materia = materia;
    }

    public String getProfesor() {
        return Profesor;
    }

    public void setProfesor(String profesor) {
        Profesor = profesor;
    }

    public String getApp() {
        return App;
    }

    public void setApp(String app) {
        App = app;
    }

    public String getApm() {
        return Apm;
    }

    public void setApm(String apm) {
        Apm = apm;
    }

    public String getGrado() {
        return Grado;
    }

    public void setGrado(String grado) {
        Grado = grado;
    }

    public String getCredito() {
        return Credito;
    }

    public void setCredito(String credito) {
        Credito = credito;
    }

    public String getClave() {
        return Clave;
    }

    public void setClave(String clave) {
        Clave = clave;
    }

    public String getOpcion() {
        return Opcion;
    }

    public void setOpcion(String opcion) {
        Opcion = opcion;
    }

    public String getDia() {
        return Dia;
    }

    public void setDia(String dia) {
        Dia = dia;
    }

    public String getHinicio() {
        return Hinicio;
    }

    public void setHinicio(String hinicio) {
        Hinicio = hinicio;
    }

    public String getHfin() {
        return Hfin;
    }

    public void setHfin(String hfin) {
        Hfin = hfin;
    }

    public String getAula() {
        return Aula;
    }

    public void setAula(String aula) {
        Aula = aula;
    }


}
